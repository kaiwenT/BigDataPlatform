package com.hust.bigdataplatform.model.params;

public class ExpScore {
	
	String expName;
	String resultsScore;
	String reportScore;
	String finalScore;
	public String getExpName() {
		return expName;
	}
	public void setExpName(String expName) {
		this.expName = expName;
	}
	public String getResultsScore() {
		return resultsScore;
	}
	public void setResultsScore(String resultsScore) {
		this.resultsScore = resultsScore;
	}
	public String getReportScore() {
		return reportScore;
	}
	public void setReportScore(String reportScore) {
		this.reportScore = reportScore;
	}
	public String getFinalScore() {
		return finalScore;
	}
	public void setFinalScore(String finalScore) {
		this.finalScore = finalScore;
	}
	
}
